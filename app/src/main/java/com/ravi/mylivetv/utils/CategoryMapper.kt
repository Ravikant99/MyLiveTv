    fun getCategoryUrl(categoryName: String): String {
        val category = categoryMap[categoryName] ?: categoryName.lowercase()
        return UrlBuilder.categories(category)
    }
    
    fun getLanguageUrl(languageName: String): String {
        val code = languageMap[languageName] ?: languageName.lowercase()
        return UrlBuilder.languages(code)
    }

    fun getCountryUrl(countryName: String): String {
        val countryCode = countryMap[countryName] ?: countryName.lowercase()
        return UrlBuilder.countries(countryCode)
    }

    fun isCategory(name: String): Boolean {
        return categoryMap.containsKey(name)
    }
    
    fun isLanguage(name: String): Boolean {
        return languageMap.containsKey(name)
    }
    
    fun isCountry(name: String): Boolean {
        return countryMap.containsKey(name)
    }
